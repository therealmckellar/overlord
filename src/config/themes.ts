export function getThemeColors(name: ThemeName): ThemeColors {
  if (name === 'system') return themes.dark;
  return themes[name];
}

export function themeToCSSVariables(name: ThemeName): Record<string, string> {
  const c = themes[name];
  return {
    '--bg': c.bg,
    '--bg-secondary': c.bgSecondary,
    '--bg-tertiary': c.bgTertiary,
    '--text': c.text,
    '--text-secondary': c.textSecondary,
    '--text-muted': c.textMuted,
    '--border': c.border,
    '--accent': c.accent,
    '--accent-hover': c.accentHover,
    '--accent-muted': c.accentMuted,
    '--success': c.success,
    '--warning': c.warning,
    '--error': c.error,
    '--info': c.info,
    '--user-bubble': c.userBubble,
    '--assistant-bubble': c.assistantBubble,
    '--code-bg': c.codeBg,
    '--code-text': c.codeText,
    '--shadow': c.shadow,
    '--overlay': c.overlay,
  };
}
